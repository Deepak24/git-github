/**
 * String - Data type
*/
let userName = "Deepak";
console.log(userName);
console.log("Type Of UserName", typeof userName);

/**
 * Number - Data Type
 */
let age = 35;
console.log(age);
console.log(typeof age);

let FraNum = 99.5;
console.log(FraNum);

/**
 * Boolean - Data Type
 */
let ifProductInCart = false;
console.log(ifProductInCart);
console.log(typeof ifProductInCart);

